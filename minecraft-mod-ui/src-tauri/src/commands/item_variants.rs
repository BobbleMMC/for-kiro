#[derive(Debug, Default, Deserialize)]
pub struct ArmorOverrides {
    pub slot: Option<ArmorSlot>,
    pub armor_material_const: Option<String>,
}

#[tauri::command]
pub async fn generate_armor_class(
    db: State<'_, DbState>,
    item_id: i64,
    slot: Option<ArmorSlot>,
    armor_material_const: Option<String>,
    overrides: Option<ArmorOverrides>,
) -> Result<GeneratedFile, String> {
    let item = db
        .0
        .get_item(item_id)
        .map_err(|e| format!("get_item: {}", e))?
        .ok_or_else(|| format!("Item {} not found", item_id))?;
    let project = db
        .0
        .get_project(item.project_id)
        .map_err(|e| format!("get_project: {}", e))?
        .ok_or_else(|| format!("Project {} not found", item.project_id))?;
    let profile = profile_for(&project.minecraft_version, &project.mod_loader);

    let ovr = overrides.unwrap_or_default();

    let slot = slot
        .or(ovr.slot)
        .or_else(|| item.armor_slot.as_deref().and_then(ArmorSlot::from_str))
        .ok_or_else(|| {
            format!(
                "Armor slot not specified and Item {} has no armor_slot persisted.",
                item_id
            )
        })?;

    let material = armor_material_const
        .or(ovr.armor_material_const)
        .or_else(|| item.armor_material.clone())
        .unwrap_or_else(|| "ArmorMaterials.IRON".into());
    if material.trim().is_empty() {
        return Err("armor_material_const cannot be empty".into());
    }

    let slot_name = slot.name();
    let class_name = format!("{}{}", pascal(&item.item_name), pascal(slot_name));
    let pkg = java_package(&item.namespace, "item");
    let header = variant_header("Armor", item_id, &profile);
    let props_init = properties_init(&profile);
    let extra_props = build_item_props_chain(&item, &profile);

    let body = format!(
        "{header}\
         package {pkg};\n\n\
         import net.minecraft.world.item.ArmorItem;\n\
         import net.minecraft.world.item.ArmorItem.Type;\n\
         import net.minecraft.world.item.ArmorMaterial;\n\
         import net.minecraft.world.item.Item;\n\n\
         /**\n\
         \x20* {display_name} ({slot_name}).\n\
         \x20*\n\
         \x20* Persisted fields used: armor_material={mat_persist:?} armor_slot={slot_persist:?}\n\
         \x20* armor_defense={def:?} armor_toughness={tough:?} knockback_resistance={kbr:?}\n\
         \x20*\n\
         \x20* TODO:\n\
         \x20*   - Replace `{mat}` with the real ArmorMaterial reference.\n\
         \x20*   - Provide a layer texture at\n\
         \x20*     assets/<modid>/textures/models/armor/<material>_layer_1.png.\n\
         \x20*   - Implement defense / toughness / knockback_resistance via the\n\
         \x20*     ArmorMaterial; the values above are stored on the Item row\n\
         \x20*     for reference but vanilla reads them from the material.\n\
         \x20*/\n\
         public final class {class} extends ArmorItem {{\n\
         \x20   public {class}() {{\n\
         \x20       super(({mat}), {slot_java}, {props}{extra});\n\
         \x20   }}\n\
         }}\n",
        header = header,
        pkg = pkg,
        display_name = item.display_name,
        slot_name = slot_name,
        mat_persist = item.armor_material,
        slot_persist = item.armor_slot,
        def = item.armor_defense,
        tough = item.armor_toughness,
        kbr = item.knockback_resistance,
        mat = material,
        class = class_name,
        slot_java = slot.java(),
        props = props_init,
        extra = extra_props,
    );

    Ok(GeneratedFile {
        file_name: format!("{}.java", class_name),
        package_path: pkg.replace('.', "/"),
        source: body,
    })
}

