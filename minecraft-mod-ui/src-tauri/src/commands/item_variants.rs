#[tauri::command]
pub async fn generate_armor_class(
    db: State<'_, DbState>,
    item_id: i64,
    slot: ArmorSlot,
    armor_material_const: String, // e.g. "ArmorMaterials.IRON" or "MyArmorMaterial.RUBY"
) -> Result<GeneratedFile, String> {
    let item = db
        .0
        .get_item(item_id)
        .map_err(|e| format!("get_item: {}", e))?
        .ok_or_else(|| format!("Item {} not found", item_id))?;

    if armor_material_const.trim().is_empty() {
        return Err("armor_material_const cannot be empty".into());
    }

    let slot_name = slot.name();
    let class_name = format!("{}{}", pascal(&item.item_name), pascal(slot_name));
    let pkg = java_package(&item.namespace, "item");

    let body = format!(
        "// =============================================================================\n\
         //  Generated by Minecraft Mod Studio — Armor variant of item id={item_id}\n\
         //  slot={slot_name} · material={mat}\n\
         // =============================================================================\n\n\
         package {pkg};\n\n\
         import net.minecraft.world.item.ArmorItem;\n\
         import net.minecraft.world.item.ArmorItem.Type;\n\
         import net.minecraft.world.item.ArmorMaterial;\n\
         import net.minecraft.world.item.Item;\n\n\
         /**\n\
         \x20* {display_name} ({slot_name}).\n\
         \x20*\n\
         \x20* TODO:\n\
         \x20*   - Replace `{mat}` with the real ArmorMaterial reference.\n\
         \x20*   - Provide a layer texture at\n\
         \x20*     assets/<modid>/textures/models/armor/<material>_layer_1.png.\n\
         \x20*/\n\
         public final class {class} extends ArmorItem {{\n\
         \x20   public {class}() {{\n\
         \x20       super(({mat}), {slot_java}, new Item.Properties());\n\
         \x20   }}\n\
         }}\n",
        item_id = item_id,
        slot_name = slot_name,
        mat = armor_material_const,
        pkg = pkg,
        display_name = item.display_name,
        class = class_name,
        slot_java = slot.java(),
    );

    Ok(GeneratedFile {
        file_name: format!("{}.java", class_name),
        package_path: pkg.replace('.', "/"),
        source: body,
    })
}

// ============================================================================
// Food
// ============================================================================

#[tauri::command]
pub async fn generate_food_class(
    db: State<'_, DbState>,
    item_id: i64,
    nutrition: i32,
    saturation_modifier: f64,
    always_eat: bool,
    can_eat_when_full: bool,
) -> Result<GeneratedFile, String> {
    let item = db
        .0
        .get_item(item_id)
        .map_err(|e| format!("get_item: {}", e))?
        .ok_or_else(|| format!("Item {} not found", item_id))?;

    if nutrition < 0 || nutrition > 20 {
        return Err(format!("Food nutrition must be 0..=20, got {}", nutrition));
    }
    if saturation_modifier < 0.0 {
        return Err("Food saturation_modifier must be >= 0".into());
    }

    let class_name = pascal(&item.item_name);
    let pkg = java_package(&item.namespace, "item");

    let mut food_builder = String::from("new FoodProperties.Builder()\n");
    food_builder.push_str(&format!("            .nutrition({})\n", nutrition));
    food_builder.push_str(&format!(
        "            .saturationMod({}f)\n",
        saturation_modifier
    ));
    if always_eat {
        food_builder.push_str("            .alwaysEat()\n");
    }
    if can_eat_when_full {
        food_builder.push_str("            .fast()\n");
    }
    food_builder.push_str("            .build()");

    let body = format!(
        "// =============================================================================\n\
         //  Generated by Minecraft Mod Studio — Food variant of item id={item_id}\n\
         //  nutrition={nutrition} · saturation={sat} · always_eat={always}\n\
         // =============================================================================\n\n\
         package {pkg};\n\n\
         import net.minecraft.world.food.FoodProperties;\n\
         import net.minecraft.world.item.Item;\n\n\
         /**\n\
         \x20* {display_name} (food).\n\
         \x20*\n\
         \x20* TODO:\n\
         \x20*   - Add MobEffectInstance entries via .effect(...) for status effects.\n\
         \x20*   - Adjust `eatAnimation` if needed.\n\
         \x20*/\n\
         public final class {class} {{\n\
         \x20   public static final FoodProperties FOOD = {food};\n\n\
         \x20   public static Item.Properties properties() {{\n\
         \x20       return new Item.Properties().food(FOOD);\n\
         \x20   }}\n\n\
         \x20   private {class}() {{}}\n\
         }}\n",
        item_id = item_id,
        nutrition = nutrition,
        sat = saturation_modifier,
        always = always_eat,
        pkg = pkg,
        display_name = item.display_name,
        class = class_name,
        food = food_builder,
    );

    Ok(GeneratedFile {
        file_name: format!("{}.java", class_name),
        package_path: pkg.replace('.', "/"),
        source: body,
    })
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn tool_kind_class_names() {
        assert_eq!(ToolKind::Pickaxe.java_class(), "PickaxeItem");
        assert_eq!(ToolKind::Sword.java_class(), "SwordItem");
    }

    #[test]
    fn armor_slot_constants() {
        assert_eq!(ArmorSlot::Head.java(), "Type.HELMET");
        assert_eq!(ArmorSlot::Chest.name(), "chestplate");
    }
}
