#[derive(Debug, Default, Deserialize)]
pub struct FoodOverrides {
    pub nutrition: Option<i32>,
    pub saturation_modifier: Option<f64>,
    pub always_eat: Option<bool>,
    pub can_eat_when_full: Option<bool>,
    pub eat_seconds: Option<f64>,
}

#[tauri::command]
pub async fn generate_food_class(
    db: State<'_, DbState>,
    item_id: i64,
    nutrition: Option<i32>,
    saturation_modifier: Option<f64>,
    always_eat: Option<bool>,
    can_eat_when_full: Option<bool>,
    overrides: Option<FoodOverrides>,
) -> Result<GeneratedFile, String> {
    let item = db
        .0
        .get_item(item_id)
        .map_err(|e| format!("get_item: {}", e))?
        .ok_or_else(|| format!("Item {} not found", item_id))?;
    let project = db
        .0
        .get_project(item.project_id)
        .map_err(|e| format!("get_project: {}", e))?
        .ok_or_else(|| format!("Project {} not found", item.project_id))?;
    let profile = profile_for(&project.minecraft_version, &project.mod_loader);

    let ovr = overrides.unwrap_or_default();

    let nutrition = nutrition.or(ovr.nutrition).unwrap_or(4);
    let saturation_modifier = saturation_modifier
        .or(ovr.saturation_modifier)
        .unwrap_or(0.3);
    let always_eat = always_eat
        .or(ovr.always_eat)
        .unwrap_or(item.food_always_eat);
    let can_eat_when_full = can_eat_when_full
        .or(ovr.can_eat_when_full)
        .unwrap_or(item.food_eat_fast);

    if !(0..=20).contains(&nutrition) {
        return Err(format!("Food nutrition must be 0..=20, got {}", nutrition));
    }
    if saturation_modifier < 0.0 {
        return Err("Food saturation_modifier must be >= 0".into());
    }

    let eat_seconds = ovr.eat_seconds.or(item.food_eat_seconds);

    let class_name = pascal(&item.item_name);
    let pkg = java_package(&item.namespace, "item");
    let header = variant_header("Food", item_id, &profile);

    let mut food_builder = String::from("new FoodProperties.Builder()\n");
    food_builder.push_str(&format!("            .nutrition({})\n", nutrition));
    food_builder.push_str(&format!(
        "            .saturationMod({}f)\n",
        saturation_modifier
    ));
    if always_eat {
        food_builder.push_str("            .alwaysEat()\n");
    }
    if can_eat_when_full {
        food_builder.push_str("            .fast()\n");
    }
    if let Some(secs) = eat_seconds {
        food_builder.push_str(&format!(
            "            // TODO 1.20.5+: .eatSeconds({}f) — only available on newer versions\n",
            secs
        ));
    }

    // Optional MobEffect lines pulled from food_effects_json. We never
    // fail the generation if this is malformed — drop a TODO comment
    // instead so the user can see what was ignored.
    let mut effect_block = String::new();
    if let Some(json) = item.food_effects_json.as_deref().filter(|s| !s.is_empty()) {
        match serde_json::from_str::<Vec<FoodEffectSpec>>(json) {
            Ok(effects) if !effects.is_empty() => {
                effect_block.push_str(
                    "            // Effects parsed from item.food_effects_json:\n",
                );
                for e in effects {
                    effect_block.push_str(&format!(
                        "            // .effect(() -> new MobEffectInstance(MobEffects.{effect}, {dur}, {amp}), {prob}f)\n",
                        effect = e.effect.to_uppercase().replace('-', "_"),
                        dur = e.duration_ticks,
                        amp = e.amplifier,
                        prob = e.probability,
                    ));
                }
            }
            _ => {
                effect_block.push_str(
                    "            // TODO: food_effects_json on the Item row could not be parsed — \n            // expected a JSON array of {effect, duration_ticks, amplifier, probability}.\n",
                );
            }
        }
    }
    food_builder.push_str(&effect_block);
    food_builder.push_str("            .build()");

    let extra_props = build_item_props_chain(&item, &profile);

    let body = format!(
        "{header}\
         package {pkg};\n\n\
         import net.minecraft.world.food.FoodProperties;\n\
         import net.minecraft.world.item.Item;\n\n\
         /**\n\
         \x20* {display_name} (food).\n\
         \x20*\n\
         \x20* Persisted fields used: food_eat_seconds={secs:?} food_always_eat={ae}\n\
         \x20* food_eat_fast={ef} food_effects_json={effects:?}\n\
         \x20*\n\
         \x20* TODO:\n\
         \x20*   - Uncomment the .effect(...) lines below if MobEffects are needed.\n\
         \x20*/\n\
         public final class {class} {{\n\
         \x20   public static final FoodProperties FOOD = {food};\n\n\
         \x20   public static Item.Properties properties() {{\n\
         \x20       return new Item.Properties().food(FOOD){extra};\n\
         \x20   }}\n\n\
         \x20   private {class}() {{}}\n\
         }}\n",
        header = header,
        pkg = pkg,
        display_name = item.display_name,
        secs = item.food_eat_seconds,
        ae = item.food_always_eat,
        ef = item.food_eat_fast,
        effects = item.food_effects_json,
        class = class_name,
        food = food_builder,
        extra = extra_props,
    );

    Ok(GeneratedFile {
        file_name: format!("{}.java", class_name),
        package_path: pkg.replace('.', "/"),
        source: body,
    })
}

#[derive(Debug, Deserialize)]
struct FoodEffectSpec {
    effect: String,
    #[serde(default)]
    duration_ticks: i32,
    #[serde(default)]
    amplifier: i32,
    #[serde(default = "default_probability")]
    probability: f64,
}

fn default_probability() -> f64 {
    1.0
}

// ============================================================================
// Shared: build the Item.Properties chain from extras
// ============================================================================
//
// Both the variant generators above and the plain `generate_item_class`
// command (PR #26 update in `codegen_commands.rs`) call this to append
// the rules persisted in PR #24 to an `Item.Properties()` expression.
//
// Returned string is a chain like
//   .stacksTo(16).rarity(Rarity.RARE).fireResistant().craftRemainder(Items.BUCKET)
// (or empty if no extras apply).
//
// Version-aware: `.attributes(...)` and `.eatSeconds(...)` are only
// appended on profiles that support them.

pub(crate) fn build_item_props_chain(
    item: &crate::db::operations::Item,
    profile: &VersionProfile,
) -> String {
    let mut out = String::new();

    // stacksTo / maxCount on Forge / NeoForge; on Fabric the Settings
    // builder uses .maxCount() but that's also the same name from 1.20.5.
    // We special-case Fabric callers in the variant code (they pass
    // Item.Settings); for Forge variants we use the standard name.
    if item.max_stack_size != 64 {
        if matches!(profile.loader, LoaderId::Fabric) {
            out.push_str(&format!(".maxCount({})", item.max_stack_size));
        } else {
            out.push_str(&format!(".stacksTo({})", item.max_stack_size));
        }
    }

    let rarity = item.rarity.to_uppercase();
    if rarity != "COMMON" && !rarity.is_empty() {
        out.push_str(&format!(".rarity(Rarity.{})", rarity));
    }

    if let Some(d) = item.durability {
        if d > 0 {
            out.push_str(&format!(".durability({})", d));
        }
    }

    if item.is_fire_resistant {
        out.push_str(".fireResistant()");
    }

    // craftRemainder = the bucket-style "leaves a thing in the grid" item.
    if let Some(rem) = item.recipe_remainder.as_deref().filter(|s| !s.is_empty()) {
        out.push_str(&format!(".craftRemainder({})", rem));
    }

    // 1.20.5+ added .attributes(...) for item-attached attribute modifiers.
    // On older profiles we leave a TODO comment instead of emitting
    // something that won't compile.
    if profile.has_item_attributes_setter {
        if let Some(_attr_json) = item
            .attribute_modifiers_json
            .as_deref()
            .filter(|s| !s.is_empty())
        {
            out.push_str(
                "/* TODO: .attributes(ItemAttributeModifiers.builder().add(...).build()) */",
            );
        }
    }

    out
}

