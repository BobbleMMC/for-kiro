//! Item-variant codegen — Tool / Armor / Food.
//!
//! All three variants are *items* in Minecraft, just with different
//! superclasses and constructors. The user picks an existing item,
//! optionally overrides a few fields, and the generator emits the
//! specialised Java class.
//!
//! As of PR #26 these generators are **version-aware** (consult
//! `feature_system::version_matrix`) and **extras-aware** (read
//! the persisted `Item` columns added in PR #24 — `tool_kind`,
//! `tool_tier`, `armor_material`, `armor_slot`, `food_eat_seconds`
//! etc.). That means the user can persist all the rules once via
//! the Item editor and call these commands with just `item_id`,
//! or supply per-call overrides for one-off generation.

use serde::{Deserialize, Serialize};
use tauri::State;

use crate::commands::codegen_commands::GeneratedFile;
use crate::feature_system::version_matrix::{profile_for, LoaderId, VersionProfile};
use super::project_commands::DbState;

#[derive(Debug, Clone, Copy, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum ToolKind {
    Pickaxe,
    Axe,
    Shovel,
    Hoe,
    Sword,
}

impl ToolKind {
    fn java_class(&self) -> &'static str {
        match self {
            ToolKind::Pickaxe => "PickaxeItem",
            ToolKind::Axe => "AxeItem",
            ToolKind::Shovel => "ShovelItem",
            ToolKind::Hoe => "HoeItem",
            ToolKind::Sword => "SwordItem",
        }
    }
    fn import(&self) -> &'static str {
        match self {
            ToolKind::Pickaxe => "import net.minecraft.world.item.PickaxeItem;",
            ToolKind::Axe => "import net.minecraft.world.item.AxeItem;",
            ToolKind::Shovel => "import net.minecraft.world.item.ShovelItem;",
            ToolKind::Hoe => "import net.minecraft.world.item.HoeItem;",
            ToolKind::Sword => "import net.minecraft.world.item.SwordItem;",
        }
    }
    fn from_str(s: &str) -> Option<ToolKind> {
        match s.trim().to_ascii_lowercase().as_str() {
            "pickaxe" => Some(ToolKind::Pickaxe),
            "axe" => Some(ToolKind::Axe),
            "shovel" => Some(ToolKind::Shovel),
            "hoe" => Some(ToolKind::Hoe),
            "sword" => Some(ToolKind::Sword),
            _ => None,
        }
    }
    fn slug(&self) -> &'static str {
        match self {
            ToolKind::Pickaxe => "pickaxe",
            ToolKind::Axe => "axe",
            ToolKind::Shovel => "shovel",
            ToolKind::Hoe => "hoe",
            ToolKind::Sword => "sword",
        }
    }
}

#[derive(Debug, Clone, Copy, Deserialize, Serialize)]
#[serde(rename_all = "snake_case")]
pub enum ArmorSlot {
    Head,
    Chest,
    Legs,
    Feet,
}

impl ArmorSlot {
    fn java(&self) -> &'static str {
        match self {
            ArmorSlot::Head => "Type.HELMET",
            ArmorSlot::Chest => "Type.CHESTPLATE",
            ArmorSlot::Legs => "Type.LEGGINGS",
            ArmorSlot::Feet => "Type.BOOTS",
        }
    }
    fn name(&self) -> &'static str {
        match self {
            ArmorSlot::Head => "helmet",
            ArmorSlot::Chest => "chestplate",
            ArmorSlot::Legs => "leggings",
            ArmorSlot::Feet => "boots",
        }
    }
    fn from_str(s: &str) -> Option<ArmorSlot> {
        match s.trim().to_ascii_lowercase().as_str() {
            "head" | "helmet" => Some(ArmorSlot::Head),
            "chest" | "chestplate" => Some(ArmorSlot::Chest),
            "legs" | "leggings" => Some(ArmorSlot::Legs),
            "feet" | "boots" => Some(ArmorSlot::Feet),
            _ => None,
        }
    }
}

const VALID_TIERS: [&str; 6] = ["WOOD", "STONE", "IRON", "GOLD", "DIAMOND", "NETHERITE"];

fn java_package(namespace: &str, sub: &str) -> String {
    let base = if namespace.contains('.') {
        namespace.to_string()
    } else {
        format!("com.modstudio.{}", namespace)
    };
    format!("{}.{}", base, sub)
}

fn pascal(s: &str) -> String {
    s.split(|c: char| !c.is_alphanumeric())
        .filter(|p| !p.is_empty())
        .map(|p| {
            let mut chars = p.chars();
            match chars.next() {
                None => String::new(),
                Some(c) => c.to_uppercase().collect::<String>() + chars.as_str(),
            }
        })
        .collect()
}

/// Build the `// Generated by …` header for a variant Java class.
/// Mirrors the format used by Block / Item / Recipe generators so the
/// user can see at a glance which (mc_version, loader) the file targets.
fn variant_header(variant: &str, item_id: i64, profile: &VersionProfile) -> String {
    format!(
        "// =============================================================================\n\
         //  Generated by Minecraft Mod Studio — {variant} variant of item id={item_id}\n\
         //  Target: {ver} · loader: {loader:?} · pack_format: {pf} · java: {jv}\n\
         // =============================================================================\n\n",
        variant = variant,
        item_id = item_id,
        ver = profile.mc_version,
        loader = profile.loader,
        pf = profile.pack_format,
        jv = profile.java_version,
    )
}

/// Pick the right `Item.Properties` chain start for the loader.
/// Forge / NeoForge use `new Item.Properties()`; Fabric uses
/// `new Item.Settings()` — but both are aliased internally to
/// `Item.Properties` from 1.20.5+. To keep the variant generators
/// loader-portable we always emit the modern `Item.Properties()`
/// since vanilla item variants (Sword/Pickaxe/Armor/...) take an
/// `Item.Properties` arg on every supported version.
fn properties_init(_profile: &VersionProfile) -> &'static str {
    "new Item.Properties()"
}

