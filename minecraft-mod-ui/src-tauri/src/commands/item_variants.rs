//! Item-variant codegen — Tool / Armor / Food.
//!
//! Tools, armor pieces and foods are all *items* in Minecraft, just with
//! different superclasses and constructors. We already have a `items`
//! table; rather than introduce three new tables, the user picks an
//! existing item and a variant kind, plus a few extra parameters
//! (tier, slot, nutrition…), and the generator emits the specialised
//! Java class. The plain `generate_item_class` command stays available
//! for "I just want a vanilla Item".
//!
//! These three commands move Tool / Armor / Food from Skeleton to
//! Partial in the Feature Catalog: they take a real DB-backed Item as
//! input and produce a compileable starter, but they intentionally
//! leave material-class wiring, slot registration, and tag handling
//! as TODOs since those involve extra files (tags, datapacks) that
//! belong in their own future PRs.

use serde::Deserialize;
use tauri::State;

use crate::commands::codegen_commands::GeneratedFile;
use super::project_commands::DbState;

#[derive(Debug, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ToolKind {
    Pickaxe,
    Axe,
    Shovel,
    Hoe,
    Sword,
}

impl ToolKind {
    fn java_class(&self) -> &'static str {
        match self {
            ToolKind::Pickaxe => "PickaxeItem",
            ToolKind::Axe => "AxeItem",
            ToolKind::Shovel => "ShovelItem",
            ToolKind::Hoe => "HoeItem",
            ToolKind::Sword => "SwordItem",
        }
    }
    fn import(&self) -> &'static str {
        match self {
            ToolKind::Pickaxe => "import net.minecraft.world.item.PickaxeItem;",
            ToolKind::Axe => "import net.minecraft.world.item.AxeItem;",
            ToolKind::Shovel => "import net.minecraft.world.item.ShovelItem;",
            ToolKind::Hoe => "import net.minecraft.world.item.HoeItem;",
            ToolKind::Sword => "import net.minecraft.world.item.SwordItem;",
        }
    }
}

#[derive(Debug, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ArmorSlot {
    Head,
    Chest,
    Legs,
    Feet,
}

impl ArmorSlot {
    fn java(&self) -> &'static str {
        match self {
            ArmorSlot::Head => "Type.HELMET",
            ArmorSlot::Chest => "Type.CHESTPLATE",
            ArmorSlot::Legs => "Type.LEGGINGS",
            ArmorSlot::Feet => "Type.BOOTS",
        }
    }
    fn name(&self) -> &'static str {
        match self {
            ArmorSlot::Head => "helmet",
            ArmorSlot::Chest => "chestplate",
            ArmorSlot::Legs => "leggings",
            ArmorSlot::Feet => "boots",
        }
    }
}

fn java_package(namespace: &str, sub: &str) -> String {
    let base = if namespace.contains('.') {
        namespace.to_string()
    } else {
        format!("com.modstudio.{}", namespace)
    };
    format!("{}.{}", base, sub)
}

fn pascal(s: &str) -> String {
    s.split(|c: char| !c.is_alphanumeric())
        .filter(|p| !p.is_empty())
        .map(|p| {
            let mut chars = p.chars();
            match chars.next() {
                None => String::new(),
                Some(c) => c.to_uppercase().collect::<String>() + chars.as_str(),
            }
        })
        .collect()
}

// ============================================================================
// Tool
// ============================================================================

#[tauri::command]
pub async fn generate_tool_class(
    db: State<'_, DbState>,
    item_id: i64,
    kind: ToolKind,
    tier: String,        // WOOD / STONE / IRON / GOLD / DIAMOND / NETHERITE
    attack_damage_bonus: f64,
    attack_speed_modifier: f64,
) -> Result<GeneratedFile, String> {
    let item = db
        .0
        .get_item(item_id)
        .map_err(|e| format!("get_item: {}", e))?
        .ok_or_else(|| format!("Item {} not found", item_id))?;

    let tier_upper = tier.to_uppercase();
    if !["WOOD", "STONE", "IRON", "GOLD", "DIAMOND", "NETHERITE"].contains(&tier_upper.as_str()) {
        return Err(format!(
            "Tool tier '{}' must be one of WOOD/STONE/IRON/GOLD/DIAMOND/NETHERITE",
            tier
        ));
    }
    if attack_speed_modifier > 0.0 {
        return Err("attack_speed_modifier should be negative or 0 (Minecraft convention)".into());
    }

    let class_name = pascal(&item.item_name);
    let pkg = java_package(&item.namespace, "item");
    let java_class = kind.java_class();
    let import = kind.import();

    let body = format!(
        "// =============================================================================\n\
         //  Generated by Minecraft Mod Studio — Tool variant of item id={item_id}\n\
         //  kind={kind:?} · tier={tier_upper} · +damage={dmg} · speed_mod={spd}\n\
         // =============================================================================\n\n\
         package {pkg};\n\n\
         {import}\n\
         import net.minecraft.world.item.Item;\n\
         import net.minecraft.world.item.Tiers;\n\n\
         /**\n\
         \x20* {display_name} ({kind_name}).\n\
         \x20*\n\
         \x20* TODO:\n\
         \x20*   - Replace `Tiers.{tier_upper}` with a custom Tier if you need\n\
         \x20*     different mining level / durability / enchantability.\n\
         \x20*   - Add this item to the matching tag (e.g. #minecraft:mineable/{kind_lower}).\n\
         \x20*/\n\
         public final class {class} extends {java_class} {{\n\
         \x20   public {class}() {{\n\
         \x20       super(Tiers.{tier_upper}, {dmg_int}, {spd}f, new Item.Properties());\n\
         \x20   }}\n\
         }}\n",
        item_id = item_id,
        kind = kind,
        tier_upper = tier_upper,
        pkg = pkg,
        import = import,
        display_name = item.display_name,
        kind_name = format!("{:?}", kind),
        kind_lower = format!("{:?}", kind).to_lowercase(),
        class = class_name,
        java_class = java_class,
        dmg = attack_damage_bonus,
        dmg_int = attack_damage_bonus as i32,
        spd = attack_speed_modifier,
    );

    Ok(GeneratedFile {
        file_name: format!("{}.java", class_name),
        package_path: pkg.replace('.', "/"),
        source: body,
    })
}

