/// Tool generation parameters. Every field is optional: when omitted,
/// the value is read from the persisted Item row (the `tool_kind` /
/// `tool_tier` columns added in PR #24).
#[derive(Debug, Default, Deserialize)]
pub struct ToolOverrides {
    pub kind: Option<ToolKind>,
    pub tier: Option<String>,
    pub attack_damage_bonus: Option<f64>,
    pub attack_speed_modifier: Option<f64>,
}

#[tauri::command]
pub async fn generate_tool_class(
    db: State<'_, DbState>,
    item_id: i64,
    // Legacy fields (kept for backwards compatibility — the React
    // editor prior to PR #26 sent these explicitly).
    kind: Option<ToolKind>,
    tier: Option<String>,
    attack_damage_bonus: Option<f64>,
    attack_speed_modifier: Option<f64>,
    // New: structured overrides blob. If both are sent, the explicit
    // fields above take precedence.
    overrides: Option<ToolOverrides>,
) -> Result<GeneratedFile, String> {
    let item = db
        .0
        .get_item(item_id)
        .map_err(|e| format!("get_item: {}", e))?
        .ok_or_else(|| format!("Item {} not found", item_id))?;
    let project = db
        .0
        .get_project(item.project_id)
        .map_err(|e| format!("get_project: {}", e))?
        .ok_or_else(|| format!("Project {} not found", item.project_id))?;
    let profile = profile_for(&project.minecraft_version, &project.mod_loader);

    let ovr = overrides.unwrap_or_default();

    // Resolve tool kind: explicit arg → overrides → DB extras.
    let kind = kind
        .or(ovr.kind)
        .or_else(|| item.tool_kind.as_deref().and_then(ToolKind::from_str))
        .ok_or_else(|| {
            format!(
                "Tool kind not specified and Item {} has no tool_kind persisted. \
                 Pass `kind` explicitly or set Tool kind in the Item editor.",
                item_id
            )
        })?;

    // Resolve tier: explicit arg → overrides → DB extras → default IRON.
    let tier_raw = tier
        .or(ovr.tier)
        .or_else(|| item.tool_tier.clone())
        .unwrap_or_else(|| "IRON".into());
    let tier_upper = tier_raw.to_uppercase();
    if !VALID_TIERS.contains(&tier_upper.as_str()) {
        return Err(format!(
            "Tool tier '{}' must be one of {}",
            tier_raw,
            VALID_TIERS.join("/")
        ));
    }

    let attack_damage = attack_damage_bonus
        .or(ovr.attack_damage_bonus)
        .or(item.attack_damage)
        .unwrap_or(3.0);
    let attack_speed = attack_speed_modifier
        .or(ovr.attack_speed_modifier)
        .unwrap_or(-2.4);
    if attack_speed > 0.0 {
        return Err("attack_speed_modifier should be negative or 0 (Minecraft convention)".into());
    }

    let class_name = pascal(&item.item_name);
    let pkg = java_package(&item.namespace, "item");
    let java_class = kind.java_class();
    let import = kind.import();
    let header = variant_header("Tool", item_id, &profile);
    let props_init = properties_init(&profile);
    let extra_props = build_item_props_chain(&item, &profile);

    let body = format!(
        "{header}\
         package {pkg};\n\n\
         {import}\n\
         import net.minecraft.world.item.Item;\n\
         import net.minecraft.world.item.Tiers;\n\n\
         /**\n\
         \x20* {display_name} ({kind_slug}, tier {tier}).\n\
         \x20*\n\
         \x20* Persisted fields used: tool_kind={tk:?} tool_tier={tt:?}\n\
         \x20* attack_damage={dmg} attack_speed={spd}\n\
         \x20*\n\
         \x20* TODO:\n\
         \x20*   - Replace `Tiers.{tier}` with a custom Tier if you need\n\
         \x20*     different mining level / durability / enchantability.\n\
         \x20*   - Add this item to the matching tag\n\
         \x20*     (e.g. #minecraft:mineable/{kind_slug}).\n\
         \x20*/\n\
         public final class {class} extends {java_class} {{\n\
         \x20   public {class}() {{\n\
         \x20       super(Tiers.{tier}, {dmg_int}, {spd}f, {props}{extra});\n\
         \x20   }}\n\
         }}\n",
        header = header,
        pkg = pkg,
        import = import,
        display_name = item.display_name,
        kind_slug = kind.slug(),
        tier = tier_upper,
        tk = item.tool_kind,
        tt = item.tool_tier,
        dmg = attack_damage,
        spd = attack_speed,
        class = class_name,
        java_class = java_class,
        dmg_int = attack_damage as i32,
        props = props_init,
        extra = extra_props,
    );

    Ok(GeneratedFile {
        file_name: format!("{}.java", class_name),
        package_path: pkg.replace('.', "/"),
        source: body,
    })
}

